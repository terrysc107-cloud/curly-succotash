  chlQuestions.filter((q) => q.chapter === chapter);

export const getCHLQuestionsByDifficulty = (difficulty: Difficulty): Question[] =>
  chlQuestions.filter((q) => q.difficulty === difficulty);

export const CHL_DOMAINS = [...new Set(chlQuestions.map((q) => q.domain))];

